  PERSONLEG.ETHNICITY AS ETHNICITY , 
  PERSONLEG.RELIGION AS RELIGION , 
  PERSONLEG.DATE_OF_BIRTH AS DATE_OF_BIRTH , 
 	ROUND( (DATE_DIFF(PERSONLEG.DATE_FROM, PARSE_DATE('%Y-%m-%d',PERSONLEG.DATE_OF_BIRTH), DAY)/365),2) AS AGE_IN_YEARS	, 
