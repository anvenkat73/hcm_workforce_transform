  PERSONLEG.LEGISLATION_CODE AS LEGISLATION_CODE , 
  PERSONLEG.DATE_FROM AS EVENT_DATE , 
  CAST(PERSONLEG.PERSON_ID AS STRING)||'~'||PERSONLEG.LEGISLATION_CODE||'~'||FORMAT_DATE('%Y%m%d',PERSONLEG.DATE_FROM) AS SOURCE_RECORD_ID , 
