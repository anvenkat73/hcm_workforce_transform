  TO_CHAR(PES.EVAL_SECTION_ID) AS SOURCE_RECORD_ID      , 
  COALESCE(PES.BUSINESS_GROUP_ID,-99999) AS BUSINESS_GROUP_ID     , 
  PES.SEQUENCE_NUMBER AS SEQUENCE_NUMBER       , 
  PES.SECTION_MIN_WEIGHT AS SECTION_MIN_WEIGHT    , 
  PES.SECTION_WEIGHT AS SECTION_WEIGHT        , 
  COALESCE(PDSVO.TEMPLATESECTIONPEOSECTIONDEFID,-99999) AS SECTION_NAME_ID       , 
  PES.SECTION_TYPE_CODE AS SECTION_TYPE_CODE     
 FROM  {{ ref('DW_PERF_EVAL_SECTION_D') }} PES INNER JOIN {{ ref('stg_h_hcmperformancesetupam_performancedocsectionpvo.tmpl') }} PDSVO  ON ( PES.TEMPLATE_SECTION_ID = PDSVO.SECTIONID AND PES.BUSINESS_GROUP_ID = PDSVO.BUSINESSGROUPID )

