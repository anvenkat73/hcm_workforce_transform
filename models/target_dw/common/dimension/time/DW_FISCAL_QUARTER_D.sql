 SELECT  FP.FISCAL_PERIOD_SET_NAME AS FISCAL_PERIOD_SET_NAME , 
  FP.FISCAL_PERIOD_TYPE AS FISCAL_PERIOD_TYPE , 
  FP.FISCAL_YEAR_NUMBER AS FISCAL_YEAR_NUMBER , 
  FP.FISCAL_QUARTER_NUMBER AS FISCAL_QUARTER_NUMBER , 
  FP.FISCAL_PERIOD_SET_NAME||'~'||FP.FISCAL_PERIOD_TYPE||'~'||FP.FISCAL_YEAR_NUMBER||'~'||FP.FISCAL_QUARTER_NUMBER AS SOURCE_RECORD_ID , 
  FP.FISCAL_HALF_NUMBER AS FISCAL_HALF_NUMBER , 
  FP.FISCAL_QUARTER_CODE AS FISCAL_QUARTER_CODE , 
  FP.FISCAL_QUARTER_START_DATE AS FISCAL_QUARTER_START_DATE , 
  FP.FISCAL_QUARTER_START_DATE_ID AS FISCAL_QUARTER_START_DATE_ID , 
  FP.FISCAL_QUARTER_END_DATE AS FISCAL_QUARTER_END_DATE , 
  FP.FISCAL_QUARTER_END_DATE_ID AS FISCAL_QUARTER_END_DATE_ID , 
  FP.FISCAL_YEAR_START_DATE AS FISCAL_YEAR_START_DATE , 
  FP.FISCAL_YEAR_START_DATE_ID AS FISCAL_YEAR_START_DATE_ID , 
  FP.FISCAL_YEAR_END_DATE AS FISCAL_YEAR_END_DATE , 
  FP.FISCAL_YEAR_END_DATE_ID AS FISCAL_YEAR_END_DATE_ID , 
  FP.FISCAL_YEAR_TOTAL_WEEKS AS FISCAL_YEAR_TOTAL_WEEKS , 
  FP.FISCAL_YEAR_TOTAL_DAYS AS FISCAL_YEAR_TOTAL_DAYS , 
  MAX(CASE WHEN FP.FISCAL_QUARTER_START_DATE=FP.FISCAL_YEAR_START_DATE THEN 'Y' ELSE 'N' END) AS FIRST_QUARTER_FISCAL_YEAR_FLG , 
  MAX(CASE WHEN FP.FISCAL_QUARTER_END_DATE=FP.FISCAL_YEAR_END_DATE THEN 'Y' ELSE 'N' END) AS LAST_QUARTER_FISCAL_YEAR_FLAG , 
  FP.CURRENT_FISCAL_QUARTER_CODE AS CURRENT_FISCAL_QUARTER_CODE , 
  FP.CURRENT_FISCAL_YEAR_CODE AS CURRENT_FISCAL_YEAR_CODE 
 FROM {{ ref('DW_FISCAL_PERIOD_D') }} FP
 WHERE 	FP.FISCAL_PERIOD_SET_NAME <> '~NOVALUE~' AND FP.ADJUSTMENT_PERIOD_FLAG <> 'Y' GROUP BY  FP.FISCAL_PERIOD_SET_NAME, FP.FISCAL_PERIOD_TYPE, FP.FISCAL_YEAR_NUMBER, FP.FISCAL_QUARTER_NUMBER, FP.FISCAL_PERIOD_SET_NAME||'~'||FP.FISCAL_PERIOD_TYPE||'~'||FP.FISCAL_YEAR_NUMBER||'~'||FP.FISCAL_QUARTER_NUMBER, FP.FISCAL_HALF_NUMBER, FP.FISCAL_QUARTER_CODE, FP.FISCAL_QUARTER_START_DATE, FP.FISCAL_QUARTER_START_DATE_ID, FP.FISCAL_QUARTER_END_DATE, FP.FISCAL_QUARTER_END_DATE_ID, FP.FISCAL_YEAR_START_DATE, FP.FISCAL_YEAR_START_DATE_ID, FP.FISCAL_YEAR_END_DATE, FP.FISCAL_YEAR_END_DATE_ID, FP.FISCAL_YEAR_TOTAL_WEEKS, FP.FISCAL_YEAR_TOTAL_DAYS, FP.CURRENT_FISCAL_QUARTER_CODE, FP.CURRENT_FISCAL_YEAR_CODE
